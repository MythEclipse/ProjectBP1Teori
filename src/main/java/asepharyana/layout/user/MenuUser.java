    public void Home() {
        User user = new User(loginSuccess);
        Konten.removeAll();
        Konten.add(user.getContentPane(), java.awt.BorderLayout.CENTER);
        Konten.revalidate();
        Konten.repaint();
    }

    public void DeleteFungsi() {
        String id = loginSuccess.getId();
        UserRepository repo = new UserRepository();
        repo.delete(id);
    }

